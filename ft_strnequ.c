#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = -1;
	if ((s1[0] && s2[0]) == '\0')
		return (1);
	if ((s1[i] && s2[i]) && n > 0)
	{
		i++;
		ft_strnequ(&s1[i], &s2[i], n--);
		return (1);
	}
	return (0);
}
